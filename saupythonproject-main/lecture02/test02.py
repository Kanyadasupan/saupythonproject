#ต่อข้มูลหลายๆประเภทเข้าด้วยกัน
#ใช้ ,
score = 500
print("Hello",20,True,654.34,18/2,"Hii",score)
#ใช้ +
print("Hello"+str(20)+str(True)+str(65.43)+str(18/2)+"hiii"+str(score))
#ใช้ เมธอด format
print("Hello {} {} {} {} hiiiii {}".format(20,True,65.43,18/2,score))
#ใช้ f-string
print(f"hello {20} {True} {65.43} {10/2} hiii {score}")
#ใช้ modular operater %
print("Hello %d %f %s Hi" % (20, 65.43, "sau") )


